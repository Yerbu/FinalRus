def create_main_keyboard():
    keyboard = types.InlineKeyboardMarkup()
    for index, topic in enumerate(it_terms.keys()):
        callback_data = json.dumps({'action': 'topic', 'index': index})
        keyboard.add(types.InlineKeyboardButton(text=topic, callback_data=callback_data))
    keyboard.add(types.InlineKeyboardButton(text="–ü–æ–∏—Å–∫ üîç", callback_data=json.dumps({'action': 'search'})))
    return keyboard

def create_terms_keyboard(topic_index):
    keyboard = types.InlineKeyboardMarkup()
    topic_name = list(it_terms.keys())[topic_index]
    for index, term in enumerate(it_terms[topic_name].keys()):
        callback_data = json.dumps({'action': 'term', 'topic_index': topic_index, 'term_index': index})
        keyboard.add(types.InlineKeyboardButton(text=term, callback_data=callback_data))
    keyboard.add(types.InlineKeyboardButton(text="–ù–∞–∑–∞–¥ ‚Ü©Ô∏è", callback_data=json.dumps({'action': 'back'})))
    return keyboard

@bot.message_handler(commands=['start'])
def send_welcome(message):
    bot.send_message(message.chat.id, "–í—ã–±–µ—Ä–∏—Ç–µ —Ç–µ–º–∞—Ç–∏–∫—É IT-—Ç–µ—Ä–º–∏–Ω–æ–≤ –∏–ª–∏ –≤–æ—Å–ø–æ–ª—å–∑—É–π—Ç–µ—Å—å –ø–æ–∏—Å–∫–æ–º:", reply_markup=create_main_keyboard())

@bot.callback_query_handler(func=lambda call: True)
def callback_query(call):
    data = json.loads(call.data)
    action = data['action']
    
    if action == 'topic':
        topic_index = data['index']
        bot.edit_message_text(chat_id=call.message.chat.id, message_id=call.message.message_id, text="–í—ã–±–µ—Ä–∏—Ç–µ —Ç–µ—Ä–º–∏–Ω:", reply_markup=create_terms_keyboard(topic_index))
    elif action == 'term':
        topic_index, term_index = data['topic_index'], data['term_index']
        topic_name = list(it_terms.keys())[topic_index]
        term_name = list(it_terms[topic_name].keys())[term_index]
        description = it_terms[topic_name][term_name]
        bot.send_message(call.message.chat.id, f"{term_name}: {description}")
    elif action == 'back':
        bot.edit_message_text(chat_id=call.message.chat.id, message_id=call.message.message_id, text="–í—ã–±–µ—Ä–∏—Ç–µ —Ç–µ–º–∞—Ç–∏–∫—É IT-—Ç–µ—Ä–º–∏–Ω–æ–≤ –∏–ª–∏ –≤–æ—Å–ø–æ–ª—å–∑—É–π—Ç–µ—Å—å –ø–æ–∏—Å–∫–æ–º:", reply_markup=create_main_keyboard())
    elif action == 'search':
        msg = bot.send_message(call.message.chat.id, "–í–≤–µ–¥–∏—Ç–µ —Ç–µ—Ä–º–∏–Ω –¥–ª—è –ø–æ–∏—Å–∫–∞:")
        bot.register_next_step_handler(msg, perform_search)

def perform_search(message):
    query = message.text.lower()
    results = "–†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞:\n"
    found = False
    for topic, terms in it_terms.items():
        for term, description in terms.items():
            if query in term.lower():
                results += f"\n{term}: {description}\n"
                found = True
    if not found:
        results = "–û—à–∏–±–∫–∞: —Ç–µ—Ä–º–∏–Ω –Ω–µ –Ω–∞–π–¥–µ–Ω."
    bot.send_message(message.chat.id, results)

